/*
    사용자를 생성
    CREATE USER [USER NAME] IDENTIFIED BY [PASSWORD];
    
*/

CREATE USER STU IDENTIFIED BY LOVE; -- 사용자 생성

ALTER USER STU IDENTIFIED BY HOPE; -- 사용자 수정(비밀번호)

DROP USER STU;  -- 사용자 삭제

GRANT CREATE SESSION TO STU; -- 사용자한테 권한 부여(CREATE SESSION)

GRANT SELECT ON SCOTT.SAL_DML TO STU; 
GRANT INSERT ON SCOTT.SAL_DML TO STU;

REVOKE SELECT, INSERT ON SCOTT.SAL_DML FROM STU; -- 권한 삭제

SELECT * FROM USER_SYS_PRIVS; -- 해당유저가 가지고 있는 권한 LIST

CREATE ROLE SCHOOL; -- 권한 룰을 생성(특정한 권한들을 한꺼번에 모아서 SCHOOL이라는 ROLE에 저장)
GRANT CONNECT, RESOURCE,CREATE VIEW TO SCHOOL;

GRANT SCHOOL TO STU;

SELECT * FROM DBA_SYS_PRIVS WHERE GRANTEE='SCHOOL';


